    oled.menu_navigation(wtr_lvl,food_lvl,food_max, cat)
    alarm.idle()
    action = oled.menu_action()
    if action == 0:
        pass
    elif action == 1:
        if food_lvl > 0:
            alarm.food_refill()
            servo.rotate(0,100)
            time.sleep(2)
            servo.rotate(0,100,-1)
    elif action == 2:
        if wtr_lvl > 0:
            alarm.water_trig_flow()
            print("Act Pump")
            water_pump.activate_pump(True)
            time.sleep(2)
            print("Deact pump")
            water_pump.activate_pump(False)
    elif action == 3:
        alarm.clear()
        oled.soft_reset()

